-- Anti-spam: лимиты по актору и скользящему окну.
-- Источники: `Conversation_Lifecycle §2`, `§23`; решение владельца D5 (2026-07-16,
-- `Architecture_Decisions_v1 §1`): в MVP только per-actor + sliding window + cooldown +
-- duplicate detection. IP, device fingerprint, risk engine, ASN, geo, TOR/VPN и
-- reputation throttling — отдельный Security Layer следующей фазы, не здесь.
--
-- РЕШЕНИЕ 1. Одна таблица событий, не счётчики.
-- Счётчик («сколько уже потрачено») пришлось бы сбрасывать, а сброс — это фиксированное
-- окно: в 23:59 потратил всё, в 00:01 снова полная квота, то есть двойная пачка за две
-- минуты. Скользящее окно, которого требует §2, считается только по событиям с временем.
-- Строки маленькие и живут не дольше окна (см. решение 4).
--
-- РЕШЕНИЕ 2. Политики живут в коде, а не в справочнике — вопреки общему правилу.
-- `CLAUDE.md` требует справочники вместо enum'ов, и `dm_policies` / `notification_types`
-- сделаны именно так. Здесь сознательное отклонение: политика лимита — не тип доменного
-- объекта, а конфигурация маршрута, и она обязана существовать ровно там, где объявлен
-- маршрут (`routes.js`), иначе объявление нечем проверить на старте. Справочник в БД
-- означал бы, что валидность объявления известна только после коннекта к базе, а F6
-- требует падать до первого запроса. Цена отклонения названа честно: поменять лимит — это
-- деплой (49 секунд), а не UPDATE. При нуле пользователей это дешевле, чем вторая точка
-- истины. Появится нужда крутить лимиты под живой атакой — справочник добавляется поверх,
-- без миграции этой таблицы.
--
-- РЕШЕНИЕ 3. Считаем попытки, а не успехи.
-- Строка пишется до того, как хендлер что-либо сделал, и не откатывается вместе с ним.
-- Иначе перебор был бы бесплатным: `POST /entities/:id/members` ищет пользователя по email
-- и отвечает 404, если такого нет, — это оракул принадлежности к платформе, и его
-- ограничивает только цена попытки. Отказ обязан стоить столько же, сколько успех.
--
-- РЕШЕНИЕ 4. Хранение — не дольше окна.
-- Строка старше самого длинного окна не значит ничего и не должна накапливаться. Чистка
-- ленивая, в том же запросе, что и списание (`rateLimitService.js`): по актору и политике,
-- то есть ровно по тому индексу, который и так используется. Отдельного job'а не нужно —
-- Background Jobs как Platform Service ещё нет, и заводить его ради этого не стоит.
create table if not exists rate_limit_events (
  id uuid primary key default gen_random_uuid(),
  -- Актор — всегда User. Не Party: лимит — это про того, кто нажал, а за вывеской
  -- сущности нельзя спрятать того, кто действует (`Lifecycle §19`, `CLAUDE.md`).
  -- Аноним сюда не попадает и не может — см. комментарий про public в `routes.js`.
  actor_user_id uuid not null references users(id) on delete cascade,
  -- Ключ политики из `RATE_LIMIT_POLICIES` (решение 2). Без FK: справочника нет намеренно.
  policy_key text not null,
  created_at timestamptz not null default now()
);

-- Единственный запрос к этой таблице: «сколько событий у актора по политике за окно» —
-- и он же чистит старое. Порядок колонок — под него.
create index if not exists rate_limit_events_actor_policy_idx
  on rate_limit_events (actor_user_id, policy_key, created_at desc);
